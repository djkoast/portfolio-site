  <div class="prose dark:prose-invert max-w-none">
    <p>I completed a comprehensive, self‑directed study of UK Copyright Fundamentals, designed to give a working musician a deep understanding of the legal framework that protects creative works. The module was structured as a 12‑week, day‑by‑day curriculum, combining legal research, practical exercises, and reflective writing.</p>

    <h2>What I Covered</h2>
    <ul>
      <li>Week 1 – The purpose and structure of UK copyright law (CDPA 1988)</li>
      <li>Week 2 – Subject matter: what types of musical works are protected</li>
      <li>Week 3 – Originality: the UK standard (skill, labour, judgment) vs EU standard</li>
      <li>Week 4 – Fixation: the recording requirement and digital‑age implications</li>
      <li>Week 5 – Authorship & first ownership (employee, freelance, commissioned works)</li>
      <li>Week 6 – Joint works and co‑authorship (Lennon‑McCartney analysis, collaboration agreements)</li>
      <li>Week 7 – Duration of copyright (musical works, sound recordings, performers' rights)</li>
      <li>Week 8 – Public domain: when works become free to use</li>
      <li>Week 9 – Moral rights (paternity, integrity, false attribution, waiver)</li>
      <li>Week 10 – Performers' rights (property and non‑property rights, Noel Redding case)</li>
      <li>Week 11 – Collecting societies (PRS, PPL, MCPS, royalty flows, Rowntree case)</li>
      <li>Week 12 – Capstone: multi‑layer copyright analysis of a real‑world scenario</li>
    </ul>

