    <h2>M5‑Powered Methodology</h2>
    <p>I designed a fully digital study system using only the apps that came with my Mac (Freeform, Notes, Numbers, GarageBand) and the local AI tool Ollama. Every session had a specific “M5 Workflow” that leveraged the M5’s Neural Engine, GPU, and unified memory to create, organise, and recall information instantly — no internet required.</p>
    <p>The result: a searchable, multimedia knowledge base of UK music copyright law, including mind maps, flowcharts, spreadsheets, audio transcriptions, and written assignments, all synced via iCloud.</p>

