    <p>A weather app that displays current conditions and a 5‑day forecast, with a macOS widget and notifications.</p>

    <h2>Features</h2>
    <ul>
      <li>Live weather from OpenWeatherMap API</li>
      <li>Celsius / Fahrenheit toggle, dark mode</li>
      <li>Current location via CoreLocation</li>
      <li>Hourly and 5‑day forecast, wind, humidity, UV index</li>
      <li>Weather alerts and animated weather icon</li>
      <li>macOS widget with data shared via App Groups</li>
    </ul>

