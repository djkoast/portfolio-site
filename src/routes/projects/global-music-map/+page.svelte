    <p>An interactive Tableau dashboard exploring the global music market. Built from a custom dataset of sales, streaming, and genre data across 50+ countries, the dashboard tells a clear story about where music is being consumed and how different genres perform by region.</p>

    <h2>What I Built</h2>
    <ul>
      <li><strong>Country Map:</strong> colour‑coded by sales status, with tooltips showing total sales, top genre, and market share.</li>
      <li><strong>Top 10 Markets Bar Chart:</strong> ranked view of the highest‑revenue countries.</li>
      <li><strong>Electronic Scenes Highlight Table:</strong> a heat‑map of electronic music popularity across regions.</li>
      <li><strong>Calculated Field:</strong> “Sales Status” – classifies each country as “Data Available” or “No Data”.</li>
      <li><strong>Dashboard:</strong> all sheets combined into a single, interactive view.</li>
    </ul>

