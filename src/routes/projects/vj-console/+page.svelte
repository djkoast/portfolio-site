    <h2>Features</h2>
    <ul>
      <li>18 distinct visual modes: Pulsing Circle, Kaleidoscope, Waveform, Fractal Noise, Particle Storm, Starfield, Tunnel, Glitch, Audio Spectrum, Pixelate, Disco Ball, Matrix Rain, Ripple, Glow Tunnel, Swirl Mirror, Space, Ocean, Mountains</li>
      <li>100 factory presets combining mode and colour, with automatic cycling</li>
      <li>Audio‑beat synchronisation via <code>triggerBeat(bpm:)</code></li>
      <li>Real‑time Core Image rendering pipeline (1920×1080)</li>
      <li>Adjustable speed, intensity, size, and rotation</li>
      <li>Built with Combine for reactive UI and timer management</li>
